package ca.bcit.main.collections;

// COLLECTIONS
// ------------------------------------------------------------------
// A collection is an object that serves as a repo for other objects
// A collection provides services for adding, removing, and managing
// the elements it contains
// Some collections are ordered, some are not
// All collection are generic

// Collections can hold a specific type
// // e.g. ArrayList<String>

// A Collection is implemented using a particular underlying data structure
// // For example, ArrayList and LinkedList both represent a list collection
// // but behave in different ways
// ==================================================================


// ABSTRACTION AND COLLECTIONS
// ------------------------------------------------------------------
// Collections are abstractions
// Collections separate the interface from the underlying complexity
// Allows us to manage complexity and makes it possible to change the
// implementation without changing the interface


// ABSTRACT DATA TYPES (ADTs)
// -----------------------------------------------------------------
// An organized collection of information and a set of operations used to
// manage that information
// The set of operations defines the interface to the ADT
// As long as the ADT fulfills the promises of the interface
// it doesn't matter how the ADT is implemented
// In essence, the terms collection and Abstract Data Type are interchangeable


